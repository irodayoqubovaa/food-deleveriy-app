export function getFoodById(id) {
    return foodItems.find((item) => item.id === id);
}
export function getFoodsByCategory(category) {
    if (category === "all")
        return foodItems;
    return foodItems.filter((item) => item.category === category);
}
export function searchFoods(query, lang) {
    const lowerQuery = query.toLowerCase();
    return foodItems.filter((item) => {
        const name = lang === "en" ? item.name : item.nameRu;
        const description = lang === "en" ? item.description : item.descriptionRu;
        return name.toLowerCase().includes(lowerQuery) || description.toLowerCase().includes(lowerQuery);
    });
}
